package ivfrt.frt.frro.web.masters.model;

public class IcpType {
	private String icp_type;
	private String icp_type_desc;
	
	
	
	
	public IcpType(String icp_type, String icp_type_desc) {
		super();
		this.icp_type = icp_type;
		this.icp_type_desc = icp_type_desc;
	}




	public String getIcp_type() {
		return icp_type;
	}




	public void setIcp_type(String icp_type) {
		this.icp_type = icp_type;
	}




	public String getIcp_type_desc() {
		return icp_type_desc;
	}




	public void setIcp_type_desc(String icp_type_desc) {
		this.icp_type_desc = icp_type_desc;
	}






	
}