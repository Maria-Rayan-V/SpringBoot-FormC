package ivfrt.frt.frro.web.masters.model;

public class JournyMode {
private String journyModeCode;
private String journyModeDesc;



public JournyMode(String journyModeCode, String journyModeDesc) {
	super();
	this.journyModeCode = journyModeCode;
	this.journyModeDesc = journyModeDesc;
}
public String getJournyModeCode() {
	return journyModeCode;
}
public void setJournyModeCode(String journyModeCode) {
	this.journyModeCode = journyModeCode;
}
public String getJournyModeDesc() {
	return journyModeDesc;
}
public void setJournyModeDesc(String journyModeDesc) {
	this.journyModeDesc = journyModeDesc;
}



}
